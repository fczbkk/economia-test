/**
 * Sum two numbers.
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function add (a, b) {
  return a + b;
}

/**
 * Subtract two numbers.
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function subtract (a, b) {
  return a - b;
}

/**
 * Multiply two numbers.
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function multiply (a, b) {
  return a * b;
}